//q13. volume of cuboid
#include <stdio.h>
int main()
{
    int length,bredth,height;
    printf(" Enter length of cuboid : ");
    scanf("%d",&length);
printf(" Enter bredth of cuboid : ");
scanf("%d",&bredth);
printf(" Enter height of cuboid : ");
scanf("%d",&height);
printf("volume of cuboid is %d",length*bredth*height);
return 0;
}
